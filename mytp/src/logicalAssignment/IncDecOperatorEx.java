package logicalAssignment;

public class IncDecOperatorEx {
	public static void main(String[] args) {
		
		int i=10;
		int j=10;
		i++;j++;
		System.out.println("i="+i);
		System.out.println("j="+j);
		
		int k=i++;
		int l=++j;
		System.out.println("k="+k);
		System.out.println("l="+l);
		
	}

}
 